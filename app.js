function handleSchoolKey(e) {
  const list = document.getElementById('school-list');
  if (!list || list.hidden) return;
  const items = list.querySelectorAll('li');
  if (!items.length) return;

  if (e.key === 'ArrowDown') {
    e.preventDefault();
    _acIndex = Math.min(_acIndex + 1, items.length - 1);
  } else if (e.key === 'ArrowUp') {
    e.preventDefault();
    _acIndex = Math.max(_acIndex - 1, 0);
  } else if (e.key === 'Enter' && _acIndex >= 0) {
    e.preventDefault();
    items[_acIndex].dispatchEvent(new MouseEvent('mousedown'));
    return;
  } else if (e.key === 'Escape') {
    list.hidden = true; _acIndex = -1;
    return;
  } else { return; }

  items.forEach((li, i) => {
    li.setAttribute('aria-selected', i === _acIndex ? 'true' : 'false');
    if (i === _acIndex) li.scrollIntoView({ block: 'nearest' });
  });
}

